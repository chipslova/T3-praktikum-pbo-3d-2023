public class Matematika {
 
    public int pertambahan(int a, int b){
        int hasil = a + b;
        return hasil;
    }
    
    public int pengurangan (int a, int b){
        int hasil = a - b;
        return hasil;
    }
    
    public int perkalian (int a, int b){
        int hasil = a * b;
        return hasil;
    }
    
    public float pembagian (int a, int b){
        float hasil =(float) a / b;
        return hasil;
    }  
}
